public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 8;
        int num2 = 6;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        double num3 = 6;
        double num4 = 10;
        System.out.println(num3 + num4);
        System.out.println(num3 - num4);
        System.out.println(num3 * num4);
        System.out.println(num3 / num4);
        System.out.println(num3 % num4);
    }
}
